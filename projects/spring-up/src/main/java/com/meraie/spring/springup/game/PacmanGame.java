package com.meraie.spring.springup.game;

public class PacmanGame implements Game {
    public void up() {
        System.out.println("Pacman Up");
    }

    public void down() {
        System.out.println("Pacman Down");
    }

    public void left() {
        System.out.println("Pacman Left");
    }

    public void right() {
        System.out.println("Pacman Right");
    }
}
